
import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOProps {
  title?: string;
  description?: string;
  keywords?: string;
  image?: string;
  url?: string;
  type?: string;
}

const SEO: React.FC<SEOProps> = ({
  title = 'Yumvi-Pay | Send Money to Africa Fast & Secure',
  description = 'Yumvi-Pay is a secure mobile app for sending money to Africa with low fees, fast transfers, and reliable service.',
  keywords = 'money transfer, send money to Africa, mobile payments, remittance, Cameroon, mobile money',
  image = '/og-image.png',
  url = 'https://yumvi-pay.app',
  type = 'website',
}) => {
  // Construct full title with base name
  const fullTitle = title.includes('Yumvi-Pay') ? title : `${title} | Yumvi-Pay`;
  
  return (
    <Helmet>
      {/* Basic Meta Tags */}
      <title>{fullTitle}</title>
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      
      {/* Open Graph / Facebook */}
      <meta property="og:type" content={type} />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={fullTitle} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={image} />
      
      {/* Twitter Card */}
      <meta name="twitter:card" content="summary_large_image" />
      <meta name="twitter:url" content={url} />
      <meta name="twitter:title" content={fullTitle} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={image} />
      
      {/* Canonical URL */}
      <link rel="canonical" href={url} />
    </Helmet>
  );
};

export default SEO;
